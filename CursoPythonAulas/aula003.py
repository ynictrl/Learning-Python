"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Nicolas Gomes')
print('Nicolas "Gomes"')

# Aspas duplas
print("Nicolas Gomes")
print("Nicolas 'Gomes'")

# Escape
print("Nicolas \"Gomes\"")

# R
print(r"Nicolas \"Gomes\"")