# Tipos int e float

# int -> Número inteiro
# O tipo int representa qualquer númeoro positivo ou negativo. 
#int sem sinal é considerado positivo.
print(11, -11, 0) # int

# float -> Número com ponto flutuante
# O tipo float representa qulaquer número positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.11, 0.0) # floar

# A função type mostra o tipo que o Python inferiu ao valor.
print(type(0), type(0.0), type('0'))